/**
 * Reference supplement pages built from the 200-supplement list
 * (CONTENT-supplements-200.md). These are the *breadth* pages: every distinct
 * supplement on the list gets its own evidence-rated page, in the same neutral,
 * non-promotional house style as the in-depth entries in `supplements.ts` (the
 * near-identical forms of one supplement — e.g. the several whey, magnesium,
 * omega-3 and creatine forms — fold into a single canonical page).
 *
 * Each entry carries a headline evidence rating (tier + basis → the medal on
 * the hub and page), a structured `body` rendered by the detail route, and a
 * primary-literature entry point. Ratings follow the list's own framing and the
 * medal ladder: `well-supported` (Gold) is reserved for supplements with strong
 * human evidence for their headline use; most sit at `preliminary` (Silver, or
 * Bronze where the evidence is largely animal/in-vitro); `marketing-claim`
 * (Unproven) marks popular claims human trials don't support. This is
 * descriptive, not a recommendation — always check interactions and regulatory
 * status before use.
 */

