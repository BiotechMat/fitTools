/** A PubMed search as the page's primary-literature entry point (house style). */
const pubmed = (term: string): Source => ({
  label: `PubMed: ${term} (research overview)`,
  url: `https://pubmed.ncbi.nlm.nih.gov/?term=${encodeURIComponent(term)}`,
});

interface Spec {
  slug: string;
  name: string;
  aka?: string[];
  category: string;
  tier: EvidenceTier;
  basis: EvidenceBasis;
  short: string;
  meta: string;
  body: string[];
  faq?: FaqEntry[];
  related?: string[];
  tools?: string[];
  safety?: { title: string; points: string[] };
  pubmed: string;
}

function ref(s: Spec): SupplementEntry {
  return {
    slug: s.slug,
    name: s.name,
    aka: s.aka,
    headlineTier: s.tier,
    headlineBasis: s.basis,
    short: s.short,
    metaDescription: s.meta,
    category: s.category,
    body: s.body,
    safety: s.safety,
    relatedSupplements: s.related ?? [],
    relatedTools: s.tools ?? [],
    faq: s.faq ?? [],
    sources: [pubmed(s.pubmed)],
  };
}

